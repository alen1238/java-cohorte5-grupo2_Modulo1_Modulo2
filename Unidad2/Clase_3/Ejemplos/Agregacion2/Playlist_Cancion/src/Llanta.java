public class Llanta {
    private String marca;
    private String modelo;
    private int tamaño;

    public Llanta(String marca, String modelo, int tamaño) {
        this.marca = marca;
        this.modelo = modelo;
        this.tamaño = tamaño;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getTamaño() {
        return tamaño;
    }

    public void setTamaño(int tamaño) {
        this.tamaño = tamaño;
    }
}
